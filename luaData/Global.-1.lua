function split(inputstr, sep)
    if sep == nil then
        sep = "%s" -- Default to whitespace
    end
    local t = {}
    for str in string.gmatch(inputstr, "([^" .. sep .. "]+)") do
        table.insert(t, str)
    end
    return t
end

